      <p>Tesla, Inc. (antigamente Tesla Motors, Inc.) é uma empresa automotiva e de armazenamento de energia norte americana que desenvolve, produz e vende automóveis elétricos de alto desempenho, componentes para motores e transmissões para veículos elétricos e produtos à base de baterias. Foi fundada em 2003 pelos engenheiros Martin Eberhard e Marc Tarpenning em San Carlos, Califórnia. O seu nome é uma homenagem ao inventor e engenheiro eletricista Nikola Tesla.

A Tesla é uma empresa privada de capital aberto que negocia na Bolsa de Valores NASDAQ sob o símbolo TSLA. No primeiro trimestre de 2013, a Tesla registrou lucro pela primeira vez na sua história. Em 2016, a marca estreou no ranking das 10 marcas automotivas mais valiosas do mundo, com um valor de marca estimado em 4,436 bilhões de dólares.

A Tesla ganhou ampla atenção após a sua produção do Tesla Roadster, o primeiro carro desportivo totalmente elétrico. O segundo veículo da empresa é o Model S, um sedã de luxo totalmente elétrico. Os seus dois próximos veículos foram os Model X e Model 3, respectivamente. Este último, assim como o crossover Model Y, lançado em 2020, obteve grande sucesso de vendas, totalizando quase 1 milhão de unidades comercializadas em 2021, destacando-se no mercado europeu, onde pela primeira vez o veículo mais vendido foi elétrico, o Tesla Model 3.

Tesla também comercializa componentes elétricos, como baterias de lítio-íon para construtoras, incluindo Daimler e Toyota. O CEO da Tesla, Elon Musk, disse que a Tesla, como um fabricante de automóveis independente, tem como objectivo oferecer carros elétricos a preços acessíveis para o consumidor de classe média.</p>
<p>A Tesla (na altura Tesla Motors) foi fundada em 2003 por Martin Eberhard e Marc Tarpenning, que financiou a empresa até à rodada Série A de investimento. Ambos desempenharam papéis ativos na empresa antes do envolvimento de Elon Musk. Foi Musk que liderou a Série A rodada de investimento, juntando-se assim em Fevereiro de 2004 ao Conselho de Administração da Tesla como seu presidente. O principal objetivo da Tesla era comercializar veículos elétricos, começando desta forma por um carro desportivo premium destinado aos seus primeiros utilizadores, e em seguida, passar o mais rapidamente possível para veículos mais tradicionais, como por exemplo sedans e compactos acessíveis.Em outubro de 2016, a Tesla anunciou que todos os carros que estava a produzir - o Model S, Model X e o novo Model 3, que foi lançado em meados de 2017 - teriam o hardware necessário para que o condutor não tenha de tocar no volante. Os modelos têm oito câmaras com visibilidade 360º a uma distância de até 250 metros à volta do carro e doze sensores ultra sônicos atualizados para "complementar a visão", permitindo que o novo sistema detecte objetos duros e moles ao dobro da distância do sistema anterior. Na frente, um radar com capacidades avançadas de processamento fornecerá dados adicionais sobre o que rodeia o automóvel numa frequência redundante, capaz de alimentar o sistema com informação visual mesmo que esteja a chover, faça nevoeiro ou haja poeira no ar.</p>
